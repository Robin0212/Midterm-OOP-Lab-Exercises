class BloodData{
    private String bloodType;
    private String rhFactor;  
    
	public void setBloodType(String bdt){
		bloodType = bdt;
	}
	public String getBloodType(){
		return bloodType;	
	}
	public void setRhFactor(String rhf){
		rhFactor = rhf;
	}
	public String getRhFactor(){
		return rhFactor;
	}
}